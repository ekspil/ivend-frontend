<script>
	import gql from 'graphql-tag';

    import { head, isEmpty } from 'ramda';
    import { areKeysNull } from '@/utils';
    import {
        validate, mapValidationObject,
        required
    } from '@/utils/validation';

	export default {
		name: 'AddController',
		data: () => ({
			equipments: [],
            revisions: [],
            input: {
                name: '',
                uid: '',
                status: "DISABLED",
                mode: "CASHLESS",
                equipmentId: 1,
                revisionId: 1
            },
            validation: {},
            status: {
                error: null,
                success: ''
            }
		}),
		apollo: {
			equipments: {
				query: gql`
					query {
						getEquipments {
							id,
							name
						}
					}
				`,
				update (data) {
					return data.getEquipments;
				}
			},
            revisions: {
                query: gql`
                    query {
                        getRevisions {
                            id,
                            name
                        }
                    }
                `,
                update (data) {
                    return data.getRevisions;
                }
            }
		},
        methods: {
            showSuccessMessage (message = 'Успешно сохранено.') {
                this.status.success = message;
                setTimeout(() => { this.status.success = ''; }, 2500);
            },
            async save () {
                const that = this;

                this.validation = mapValidationObject(validate(this.input, {
                    uid: [required],
                    name: [required]
                }));

                if (areKeysNull(this.validation)) {
                    try {
                        const { errors, data } = await this.$apollo.mutate({
                            mutation: gql`
                                mutation saveController ($data: CreateControllerInput!) {
                                    createController (input: $data) {
                                        name
                                    }
                                }
                            `,
                            variables: {
                                data: this.input
                            }
                        });

                        if (errors && !isEmpty(errors)) {
                            this.status.error = head(errors).message || 'Ошибка сохранения.';
                        } else {
                            this.showSuccessMessage();
                            setTimeout(() => { that.$router.push('/controllers'); }, 1000);
                        }
                    } catch (error) {
                        this.status.error = error.message || 'Ошибка сохранения.';
                    }
                }
            }
        }
	}
</script>
